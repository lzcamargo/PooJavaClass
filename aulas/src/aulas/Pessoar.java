package aulas;

public class Pessoar {
	public String nome;
	private int idade;
	private double peso;
	protected String cidade;
	
	public String getNome() {
		return nome;
	}
	
	public int getIdade() {
		return idade;
	}
	
	public void setIdade(int tmpvida) {
		idade = tmpvida;
	}
	
	public double getPeso() {
		return peso;
	}
	
	public void setPeso(double ppessoa) {
		peso = ppessoa;
	}
	
	public String getCidade() {
		return cidade;
	}

}
